// Comparison Operators 

// Equal 
'3' == 3; // checking for the vales 

// Strictly equal 
3 === 3 // strictly equals checking if the data types match // most of the time use triple equal time

// Not Equal 
'3' != 4; 

//strictly not equal 
'3' !== 3

// Greater than 
3 > 2;

// Less than 
2 < 3;

// Greater than or equal to 
3 >= 2; // 3 => 2 fat arrow function / use angle bracket followed by the equal sign 

// Less than or equal to 
2 <= 3;

// And 
2 && 3 

// Or 
2 || 3 

